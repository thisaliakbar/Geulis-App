    private void changePanel() {
        if(slide.equals("Slide-1")) {
            showPanel(panel1);
        } else if(slide.equals("Slide-2")) {
            showPanel(panel2);   
        } else if(slide.equals("Slide-3")) {
            showPanel(panel3);
        }
    }
    
    private void showPanel(JPanel pn) {
        panel.removeAll();
        panel.add(pn);
        panel.repaint();
        panel.revalidate();
    }
    
    
